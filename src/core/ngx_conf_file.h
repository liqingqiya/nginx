struct ngx_command_s {
    ngx_str_t             name;
    ngx_uint_t            type;
    char               *(*set)(ngx_conf_t *cf, ngx_command_t *cmd, void *conf);
    ngx_uint_t            conf;
    ngx_uint_t            offset;
    void                 *post;
};      /*该结构描述模块支持的指令，一个指令对应一个配置指令。*/

#define ngx_null_command  { ngx_null_string, 0, NULL, 0, 0, NULL }  /*初始化一个空指令*/


struct ngx_open_file_s {
    ngx_fd_t              fd;
    ngx_str_t             name;

    void                (*flush)(ngx_open_file_t *file, ngx_log_t *log);
    void                 *data;
};


#define NGX_MODULE_V1          0, 0, 0, 0, 0, 0, 1
#define NGX_MODULE_V1_PADDING  0, 0, 0, 0, 0, 0, 0, 0

struct ngx_module_s {
    ngx_uint_t            ctx_index;        /*分类标志*/
    ngx_uint_t            index;            /*模块计数器*/

    ngx_uint_t            spare0;           /*预留*/
    ngx_uint_t            spare1;
    ngx_uint_t            spare2;
    ngx_uint_t            spare3;

    ngx_uint_t            version;          /*版本*/

    void                 *ctx;               /*对应一类模块的统一接口，比如http模块，就是ngx_http_module_t类型的结构*/
    ngx_command_t        *commands;        /*模块支持的指令集合*/
    ngx_uint_t            type;             /*模块的种类*/
                                                            /*以下为回调函数*/
    ngx_int_t           (*init_master)(ngx_log_t *log);     /*主进程初始化时调用*/

    ngx_int_t           (*init_module)(ngx_cycle_t *cycle);     /*模块初始化时调用*/

    ngx_int_t           (*init_process)(ngx_cycle_t *cycle);        /*工作进程初始化时调用*/
    ngx_int_t           (*init_thread)(ngx_cycle_t *cycle);     /*线程初始化时候调用*/
    void                (*exit_thread)(ngx_cycle_t *cycle);     /*线程退出时调用*/
    void                (*exit_process)(ngx_cycle_t *cycle);        /*退出工作进程时候调用*/

    void                (*exit_master)(ngx_cycle_t *cycle);     /*退出主进程时候调用*/

    uintptr_t             spare_hook0;
    uintptr_t             spare_hook1;
    uintptr_t             spare_hook2;
    uintptr_t             spare_hook3;
    uintptr_t             spare_hook4;
    uintptr_t             spare_hook5;
    uintptr_t             spare_hook6;
    uintptr_t             spare_hook7;
};      /*对应一个模块*/


typedef struct {
    ngx_str_t             name;  /*核心模块的名字*/
    void               *(*create_conf)(ngx_cycle_t *cycle); /*解析配置项前，nginx框架会调用create_conf方法*/
    char               *(*init_conf)(ngx_cycle_t *cycle, void *conf);/*解析配置后，nginx框架会调用init_conf方法*/
} ngx_core_module_t;        /*core模块的上下文结构*/


typedef struct {
    ngx_file_t            file;  /*文件结构体*/
    ngx_buf_t            *buffer;  /*buf ??? todo*/
    ngx_uint_t            line;
} ngx_conf_file_t;          /*配置文件的结构体*/


typedef char *(*ngx_conf_handler_pt)(ngx_conf_t *cf,
    ngx_command_t *dummy, void *conf);


struct ngx_conf_s {
    char                 *name;     /*存放当前解析到的指令*/
    ngx_array_t          *args;     /*存放所有的参数*/

    ngx_cycle_t          *cycle; /*本次循环周期, 代表一次nginx运行周期*/
    ngx_pool_t           *pool;
    ngx_pool_t           *temp_pool;    /*临时的内存池，解析完参数之后即释放*/
    ngx_conf_file_t      *conf_file;    /*配置文件的结构体信息*/
    ngx_log_t            *log;

    void                 *ctx; /*指向一个上下文结构 ngx_http_conf_ctx_t, 保存http所有模块的main，server，location配置级别的配置结构*/
    ngx_uint_t            module_type;
    ngx_uint_t            cmd_type;

    ngx_conf_handler_pt   handler; /*conf回调函数*/
    char                 *handler_conf;
};


